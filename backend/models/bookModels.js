import mongoose from "mongoose";

// creating a schema
const bookSchema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    author: {
      type: String,
      required: true,
    },
    publishYear: {
      type: Number,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

// creating model
export const Book = mongoose.model("Cat", bookSchema);
